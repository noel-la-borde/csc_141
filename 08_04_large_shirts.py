# Large Shirts

def make_shirt(shirt_size, shirt_message):
    print(f"I have a {shirt_size} shirt.")
    print(f"My shirt's message is {shirt_message.title()}.")

make_shirt(shirt_size= 'Large', shirt_message= 'I love Python')

make_shirt(shirt_size= 'Medium', shirt_message= 'I love Python')

make_shirt(shirt_size= 'Small', shirt_message= 'Health is wealth')

