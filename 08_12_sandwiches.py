# Sandwiches 

def make_sandwich(*ingredients):

    print("\n Make a sandwich with the following ingredients:")
    for ingredient in ingredients:
        print(f"- {ingredient}")

make_sandwich('Bread', 'Cheese', 'Ham')
make_sandwich('bread','Pork','Ketchup','Mayonnaise')
make_sandwich('bread','Beef', 'Cheese','Ketchup','Mayonnaise')


