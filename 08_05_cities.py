# Cities 

def describe_city(city, city_country='Florida'):
    print(f" The city of {city.title()} is in {city_country.title()}.")

describe_city(city='Orlando', city_country='Florida')
describe_city('Harrisburg', 'Pennsylvania')
describe_city('Brooklyn', 'New York')

