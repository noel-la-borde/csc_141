# My Pizzas, Your Pizzas

# Original list
pizzas= ['Cheese', 'Hawaiian', 'Pepperoni', 'Vegetables', 'Sausage']
# New list
friend_pizza= ['Cheese', 'Hawaiian', 'Pepperoni', 'Vegetables', 'Mushrooms']

# My favorite pizzas from the original list
print ("My favorite pizzas are\n")
for pizza in pizzas:
    print (pizza)

# My friend's favorite pizzas list from the new list
print ("\nMy friend's favorite pizzas are:\n")
for pizza in friend_pizza:
    print (pizza)



